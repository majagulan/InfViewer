package model;

/**
 * Klasa <code>FK</code> sluzi za modelovanje stranog kljuca.
 *
 */

public class FK {
	
	private String parentTable;
	private String childTable;
	private String parentColumn;
	private String childColumn;
	
	/**
	 * Konstruktor sa parametrima klase {@link FK}
	 * @param parentTable
	 * @param childTable
	 * @param parentColumn
	 * @param childColumn
	 */
	
	public FK(String parentTable, String childTable, String parentColumn, String childColumn) {
		
		this.parentTable = parentTable;
		this.childTable = childTable;
		this.parentColumn = parentColumn;
		this.childColumn = childColumn;
	}

	/**
	 * Preuzimanje vrednosti komponente parentTable.
	 * @return parentTable
	 */

	public String getParentTable() {
		return parentTable;
	}

	/**
	 *  Dodeljivanje vrednosti komponente parentTable.
	 *  @param parentTable
	 *  @return void
	 */
	
	public void setParentTable(String parentTable) {
		this.parentTable = parentTable;
	}

	/**
	 * Preuzimanje vrednosti komponente childTable.
	 * @return childTable
	 */

	public String getChildTable() {
		return childTable;
	}

	/**
	 *  Dodeljivanje vrednosti komponente childTable.
	 *  @param childTable
	 *  @return void
	 */

	public void setChildTable(String childTable) {
		this.childTable = childTable;
	}

	/**
	 * Preuzimanje vrednosti komponente parentColumn.
	 * @return parentColumn
	 */
	
	public String getParentColumn() {
		return parentColumn;
	}

	/**
	 *  Dodeljivanje vrednosti komponente parentColumn.
	 *  @param parentColumn
	 *  @return void
	 */
	
	public void setParentColumn(String parentColumn) {
		this.parentColumn = parentColumn;
	}

	/**
	 * Preuzimanje vrednosti komponente childColumn.
	 * @return childColumn
	 */

	public String getChildColumn() {
		return childColumn;
	}

	/**
	 *  Dodeljivanje vrednosti komponente childColumn.
	 *  @param childColumn
	 *  @return void
	 */
	
	public void setChildColumn(String childColumn) {
		this.childColumn = childColumn;
	}


	@Override
	public String toString() {
		return "FK [parentTable=" + parentTable + ", childTable=" + childTable + ", parentColumn=" + parentColumn
				+ ", childColumn=" + childColumn + "]";
	}
	
	
	
	
	
}
