package tree;

import javax.swing.tree.DefaultMutableTreeNode;


/**
 * U ovoj klasi se nalaze konstruktori i get i set metode u vezi sa cvorovima stabla.
 * Klasa nasledjuje {@link DefaultMutableTreeNode}.
 *
 *@see DefaultMutableTreeNode
 */


public class TreeNode extends DefaultMutableTreeNode{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private DefaultMutableTreeNode node;
	private String idNode;
	
	/**
	 * Konstruktor bez parametara {@link TreeNode}.
	 */
	
	public TreeNode(){
		
	}
	
	/**
	 * Konstruktor sa parametrima {@link TreeNode}.
	 * @param idNode
	 * @param node
	 */
	
	public TreeNode(String idNode, DefaultMutableTreeNode node) {
		
		this.node = node;
		this.idNode = idNode;
	}
	
	/**
	 * Preuzimanje vrednosti komponente node.
	 * @see DefaultMutableTreeNode
	 * @return node
	 */

	public DefaultMutableTreeNode getNode() {
		return node;
	}

	/**
	 *  Dodeljivanje vrednosti komponente node.
	 *  @see DefaultMutableTreeNode
	 *  @param node
	 *  @return void
	 */
	
	public void setNode(DefaultMutableTreeNode node) {
		this.node = node;
	}
	
	/**
	 * Preuzimanje vrednosti komponente idNode.
	 * @return idNode
	 */

	public String getIdNode() {
		return idNode;
	}
	
	/**
	 *  Dodeljivanje vrednosti komponente idNode.
	 *  @param idNode
	 *  @return void
	 */

	public void setIdNode(String idNode) {
		this.idNode = idNode;
	}
	
	
	

}
